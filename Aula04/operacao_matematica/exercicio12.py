# Crie um programa que peça ao usuário que digite 2 números reais(float) e mostre:
# 
# A soma dos dois
# A multiplicação dos dois
# A divisão do primeiro com o segundo
# A subtração dos dois

print("vou pedir-lhe dois numeros")
n1 = float(input("digite o primeiro numero: "))
n2 = float(input("digite o primeiro numero: "))

print("a soma é: "+ str(n1+n2))
print("a multiplicação é: "+ str(n1*n2))
print("a divisão é: "+ str(n1/n2))
print("a subtração é: "+ str(n1-n2))


