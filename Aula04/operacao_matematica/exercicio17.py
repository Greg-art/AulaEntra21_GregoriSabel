# Exercicio 17
# A formula de calculo de área de um circulo é:
# 
# area = pi*r²
# 
# Sabemos que:
# 
# pi = 3.14
# r = raio da circunferência em metros (float)
# 
# Crie um programa que peça ao usuário o raio e calcule a área da circunferência
# 

raio = int(input("Qual o raio da esfera que você tem em mente? "))

area = 3.14 * raio * raio

print("a area de sua circunferencia sera: "+ str(area))